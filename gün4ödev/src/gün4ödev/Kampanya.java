package gün4ödev;

public class Kampanya {
	
	
	private int id;
	private String kampanyaAdi;
	private String kampanyaNot;
	
	
	public Kampanya() {
		
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getKampanyaAdi() {
		return kampanyaAdi;
	}
	public void setKampanyaAdi(String kampanyaAdi) {
		this.kampanyaAdi = kampanyaAdi;
	}
	public String getKampanyaNot() {
		return kampanyaNot;
	}
	public void setKampanyaNot(String kampanyaNot) {
		this.kampanyaNot = kampanyaNot;
	}
	

}
